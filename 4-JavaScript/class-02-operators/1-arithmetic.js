let num1 = 81; // Declara uma variável chamada num1 e atribui o valor 81 a ela.
let num2 = 3; // Declara uma variável chamada num2 e atribui o valor 3 a ela.

//* Soma
console.log(num1 + num2); // Imprime a soma de num1 e num2.
//* Subtração
console.log(num1 - num2); // Imprime a subtração de num1 por num2.
//* Produto de uma multiplicação(resultado)
console.log(num1 * num2); // Imprime o produto de num1 e num2.
//* Quociente de uma divisão(resultado)
console.log(num1 / num2); // Imprime o quociente de num1 por num2.
//* Resto de uma divisão
console.log(num1 % num2); // Imprime o resto da divisão de num1 por num2.
//* Potência
console.log(num1 ** num2); // Imprime num1 elevado à potência num2.
//* Raiz quadrada
console.log(num1 ** (1 / 2)); // Imprime a raiz quadrada de num1.
console.log(Math.sqrt(num1)); // Outra maneira de imprimir a raiz quadrada de num1.


console.log(num2); // Imprime o valor de num2.
console.log(num2++); // Imprime o valor de num2, e então incrementa num2.
console.log(num2); // Imprime o valor atual de num2.
console.log(++num2); // Incrementa num2, e então imprime o valor de num2.

let num = 2;
console.log(num); // Imprime o valor de num.
console.log(num--); // Imprime o valor de num, e então decrementa num.
console.log(num); // Imprime o valor ( atual ) de num.
console.log(--num); // Decrementa num, e então imprime o valor de num.

//todo As próximas linhas repetem o processo de decrementar num e imprimir seu valor.

console.log(++num + 2); // Incrementa num, e então adiciona 2 ao resultado antes de imprimir.

// Assignment

let number; // Declara uma variável chamada number.

number = 3; // Atribui o valor 3 a number.

number += 2; // Adiciona 2 ao valor de number.
number -= 2; // Subtrai 2 do valor de number.
number *= 2; // Multiplica o valor de number por 2.
number /= 2; // Divide o valor de number por 2.
number %= 2; // Atribui o resto da divisão de number por 2 a number.
number **= 2; // Eleva number à potência 2.

console.log(number); // Imprime o valor atual de number.
